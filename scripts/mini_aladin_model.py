def _rsi(s: pd.Series, n=14):
    d = s.diff()
    up, dn = d.clip(lower=0), -d.clip(upper=0)
    rs = up.rolling(n).mean() / dn.replace(0,np.nan).rolling(n).mean()
    return 100 - (100/(1+rs))


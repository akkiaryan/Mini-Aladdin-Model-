def score_text(text: str) -> float:
    txt = (text or "").strip()
    if not txt: return 0.5
    if SENTIMENT:
        out = SENTIMENT(txt[:480])
        lab = out[0]["label"].lower()
        p = float(out[0]["score"])
        return p if lab == "positive" else (1.0 - p)
    comp = VADER.polarity_scores(txt)["compound"]
    return 0.5 * (comp + 1.0)

